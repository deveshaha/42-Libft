#include <libft.h>

void	ft_bzero(void *s, size_t n)
{
	int				i;
	int				l;
	unsigned char	*c;

	i = 0;
	l = n;
	c = s;
	while (c[i] != '\0' && (i < l))
	{
		c[i] = '\0';
		i++;
	}
}
